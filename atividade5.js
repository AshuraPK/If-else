//5. Escreva um programa que verifique se uma letra fornecida pelo usuário é uma vogal ou uma consoante.

letter = "A"

if(letter.toLowerCase() == "A" | "E" | "I" | "O" | "U"){
    console.log("É vogal")
}
else{
    console.log("Será consoante")
}
// toLowerCase() é uma ferramente que fara como que o script reconheça a string de forma maiuscula ou minuscula