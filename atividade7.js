num1 = 22
num2 = 2
operacao = "*" 

if (operacao == '*'){
console.log ("o resultado da soma é:",num1*num2)
}
else if (operacao == '+'){
    console.log("o resultado da subtração é",num1+num2)
}
else if (operacao == '-'){
    console.log("o resultado da subtração é",num1-num2)
}
else if (operacao == '/'){
    console.log("o resultado da subtração é",num1/num2)
}
